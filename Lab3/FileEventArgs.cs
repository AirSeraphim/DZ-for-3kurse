using System; // Подключаем базовые классы .NET, включая EventArgs

namespace domashka3
{
    /// Класс, содержащий данные для события поиска файла.
    /// Наследуется от EventArgs — стандартной базы для аргументов событий в .NET.
    /// Передаёт информацию о найденном файле и позволяет управлять процессом поиска.
   
    public class FileEventArgs : EventArgs
    {
         
        /// Имя (или полный путь) найденного файла.
        /// Доступно только для чтения после создания объекта.
         
        public string FileName { get; }

         
        /// Флаг, указывающий, нужно ли отменить дальнейший поиск файлов.
        /// Может быть установлен в true обработчиком события, чтобы остановить выполнение.
        /// Доступен для чтения и записи.
         
        public bool Cancel { get; set; }

         
        /// Конструктор класса FileEventArgs.
        /// Инициализирует имя файла и устанавливает начальное значение флага отмены.

        public FileEventArgs(string fileName)
        {
            // Устанавливаем имя файла, полученное при вызове события
            FileName = fileName;

            // По умолчанию отмена не установлена — поиск продолжается
            Cancel = false;
        }
    }
}